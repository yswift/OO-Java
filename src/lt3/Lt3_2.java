package lt3;

public class Lt3_2 {
	public static void main(String[] args) {
		int x = 10;
		int y = 3;
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println("x % y = " + (x % y)); 
	}
}
